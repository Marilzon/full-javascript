const myAverage = 'A'

/* A switch statement. It is a conditional statement that checks if the value of myAverage is equal to
the value of the case. If it is, it will execute the code inside the case. If it is not, it will
check the next case. If none of the cases are true, it will execute the code inside the default
case. */
switch (myAverage) {
	case 'A':
		console.log("A: Nice! you are the best!")
		break
	case 'B':
		console.log("B: Wow, to the stars!")
		break
	case 'C':
		console.log("C: Ok!")
		break
	default:
		"Less average, study more to up for next level!"
}
